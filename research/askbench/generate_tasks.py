def validate(tasks):
    """Validate distribution and format."""
    from collections import Counter

    domain_label = Counter()
    for task in tasks:
        d = task["task_id"].split("-")[0]
        domain_label[(d, task["gold_label"])] += 1

    print(f"\nTotal tasks: {len(tasks)}")
    print(f"\n{'Domain':<6} {'Act':>4} {'Ask':>4} {'Refuse':>6} {'Total':>6} {'Expected':>8}")
    print("-" * 42)

    total_generated = 0
    total_expected = 0
    for domain in ["D1", "D2", "D3", "D4", "D5", "D6", "D7", "D8"]:
        exp_total, exp_act, exp_ask, exp_refuse = DISTRIBUTION[domain]
        act = domain_label.get((domain, "act"), 0)
        ask = domain_label.get((domain, "ask"), 0)
        refuse = domain_label.get((domain, "refuse"), 0)
        total = act + ask + refuse
        total_generated += total
        total_expected += exp_total

        status = "✓" if total == exp_total else f"({exp_act}/{exp_ask}/{exp_refuse})"
        print(f"{domain:<6} {act:>4} {ask:>4} {refuse:>6} {total:>6} {exp_total:>8} {status}")

    print("-" * 42)
    print(f"{'Total':<6} {'':>4} {'':>4} {'':>6} {total_generated:>6} {total_expected:>8}")

    # Check for duplicate task_ids
    ids = [t["task_id"] for t in tasks]
    dupes = [id for id in ids if ids.count(id) > 1]
    if dupes:
        print(f"\n⚠️  Duplicate task_ids: {set(dupes)}")

    # Check ask tasks have gold_question
    missing_q = [t["task_id"] for t in tasks if t["gold_label"] == "ask" and "gold_question" not in t]
    if missing_q:
        print(f"\n⚠️  Ask tasks missing gold_question: {missing_q}")


