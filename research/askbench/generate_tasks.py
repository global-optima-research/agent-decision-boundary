def t(task_id, domain, instruction, context, pending_action,
      gold_label, risk_level, gold_reason, gold_question=None):
    """Helper to build a task dict."""
    d = {
        "task_id": task_id,
        "domain": DOMAIN_NAMES[domain[:2]],
        "instruction": instruction,
        "context": context,
        "pending_action": pending_action,
        "gold_label": gold_label,
        "risk_level": risk_level,
        "gold_reason": gold_reason,
    }
    if gold_question:
        d["gold_question"] = gold_question
    return d


